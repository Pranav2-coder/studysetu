export const areas = [...new Set(institutes.map(i => i.area))].sort();

export const allSubjects = [...new Set(institutes.flatMap(i => i.subjects))].sort();

export const categories = ['Tuition Classes', 'Computer Classes'];

export const budgetRanges = [
  { label: 'Under ₹1,000', min: 0, max: 1000 },
  { label: '₹1,000 – ₹3,000', min: 1000, max: 3000 },
  { label: '₹3,000 – ₹5,000', min: 3000, max: 5000 },
  { label: '₹5,000+', min: 5000, max: Infinity },
];

export function getInstituteBySlug(slug) {
  return institutes.find(i => i.slug === slug);
}

export function getFeaturedInstitutes() {
  return institutes.filter(i => i.featured);
}

export function filterInstitutes({ category, area, subject, budgetRange }) {
  return institutes.filter(institute => {
    if (category && institute.category !== category) return false;
    if (area && institute.area !== area) return false;
    if (subject && !institute.subjects.includes(subject)) return false;
    if (budgetRange) {
      const range = budgetRanges.find(r => r.label === budgetRange);
      if (range && (institute.feesValue < range.min || institute.feesValue >= range.max)) return false;
    }
    return true;
  });
}
